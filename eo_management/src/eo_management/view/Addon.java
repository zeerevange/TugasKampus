    public void dataTable() {
    Object[] header = {"ID Addon", "Nama Addon", "Harga Addon", "Include", "Deskripsi","ID Subkategori"};
    tabmode = new DefaultTableModel (null, header);
    try {
        String sql = "SELECT * FROM addon ORDER BY id ASC";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet hasil = ps.executeQuery();
             while (hasil.next()) {
                tabmode.addRow(new Object[] {
                hasil.getString(1),
                hasil.getString(2),
                hasil.getString(3),
                hasil.getString(4),
                hasil.getString(5),
                hasil.getString(6),
             });
        }
        tabelAddon.setModel(tabmode);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "data gagal dipanggil. Pesan error : " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
